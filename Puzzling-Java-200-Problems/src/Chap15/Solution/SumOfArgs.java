/**
 =======================================================================================
 ------------------------------------ Problem 15-14 ------------------------------------
 커맨드라인 인수로 부여된 값의 합계를 구하는 프로그램을 작성하자.
 확장 for문을 사용한다.

 ----------------------------------------- 입력 -----------------------------------------
 java SumOfArgs 5.5 3.75 2.25

 ------------------------------------- 출력(실행결과) -------------------------------------
 합계는 11.5입니다.

 --------------------------------------- 에러 내용 ---------------------------------------
 없음

 =======================================================================================
 */
package Chap15.Solution;

public class SumOfArgs {
    public static void main(String[] args) {
        double sum = 0.0;
        for (String s: args) {
            sum += Double.parseDouble(s);
        }
        System.out.println("합계는 " + sum + "입니다.");
    }
}
