/**
 =======================================================================================
 ------------------------------------ Problem 15-13 ------------------------------------
 커맨드라인 인수로 부여된 값을 반지름으로 하는 원의 둘레와 넓이를 구하자.

 ----------------------------------------- 입력 -----------------------------------------
 java Circle 5.5

 ------------------------------------- 출력(실행결과) -------------------------------------
 반지름 5.5인 원의 둘레는 34.56이고 넓이는 95.03입니다.

 --------------------------------------- 에러 내용 ---------------------------------------
 없음

 =======================================================================================
 */
package Chap15.Solution;

public class Circle {
    public static void main(String[] args) {
        double r = Double.parseDouble(args[0]);
        System.out.printf("반지름 %.2f인 원의 둘레는 %.2f이고 넓이는 %.2f입니다.\n",
                r, 2 * Math.PI * r, Math.PI * r * r);
    }
}
