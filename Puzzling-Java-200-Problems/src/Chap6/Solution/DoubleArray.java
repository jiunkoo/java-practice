/**
=======================================================================================
------------------------------------- Problem 6-1 -------------------------------------
배열을 생성하고 모든 요소를 출력하는 프로그램을 작성하자.
- 자료형: double
- 크기: 5

----------------------------------------- 입력 -----------------------------------------
없음

------------------------------------- 출력(실행결과) -------------------------------------
a[0] = 0.0
a[1] = 0.0
a[2] = 0.0
a[3] = 0.0
a[4] = 0.0

--------------------------------------- 에러 내용 ---------------------------------------
없음

=======================================================================================
*/
package Chap6.Solution;

// 요소 수가 5이고 double인 배열
class DoubleArray {
    public static void main(String[] args) {
        double[] a = new double[5];
        
        for (int i = 0; i < 5; i++) {
            System.out.printf("a[%d] = %f\n", i, a[i]);
        }
    }
}
