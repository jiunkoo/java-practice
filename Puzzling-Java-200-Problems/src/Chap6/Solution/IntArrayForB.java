package Chap6.Solution;

// 배열의 각 요소에 5, 4, 3, 2, 1을 대입해서 표시(방법2: 선언 후 반복문으로 초기화)
class IntArrayForB {
    public static void main(String[] args) {
        int[] a = new int[5];

        for (int i = 0; i < 5; i++) {
            a[i] = 5 - i;
        }

        for (int i = 0; i < 5; i++) {
            System.out.printf("a[%d] = %d\n", i, a[i]);
        }
    }
}
