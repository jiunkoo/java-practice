package Chap6.Solution;

// 배열의 각 요소에 5, 4, 3, 2, 1을 대입해서 표시(방법1: 선언과 동시에 초기화)
class IntArrayForA {
    public static void main(String[] args) {
        int[] a = {5, 4, 3, 2, 1};

        for (int i = 0; i < 5; i++) {
            System.out.printf("a[%d] = %d\n", i, a[i]);
        }
    }
}
