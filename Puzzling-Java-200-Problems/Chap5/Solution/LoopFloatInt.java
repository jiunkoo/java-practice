/**
=======================================================================================
------------------------------------- Problem 5-8 -------------------------------------
float형 변수와 int형 변수를 계산한 값을 표시하자.
값은 처음 두 값과 마지막 두 값만 출력한다.
- float형 변수는 0.0부터 1.0까지 0.001씩 증가
- int형 변수는 0부터 1000까지 1씩 증가 후 1000으로 나눈 실수

----------------------------------------- 입력 -----------------------------------------
없음

------------------------------------- 출력(실행결과) -------------------------------------
float        int
-----------------------
0.0000000    0.0000000
0.0010000    0.0010000
0.9989907    0.9990000
0.9999907    1.0000000

--------------------------------------- 에러 내용 ---------------------------------------
없음

=======================================================================================
*/
// 0.0부터 1.0까지 0.001 단위로 증가시키며 표시
class LoopFloatInt {
    public static void main(String[] args) {
        System.out.println("float        int");
        System.out.println("-----------------------");

        float x = 0.0F;
        for (int i = 0; i <= 1000; i++, x += 0.001F) {
            if (i <= 1 || i >= 999) {
                System.out.printf("%9.7f    %9.7f\n", x, (float)i / 1000);
            }
        }
    }
}
