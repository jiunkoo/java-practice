// 8진수, 10진수, 16진수 12를 각각 10진수로 표시
class Print12 {
    public static void main(String[] args) {
        System.out.println("8진수 12는 10진수로" + 012 + "입니다.");
        System.out.println("8진수 12는 10진수로" + 12 + "입니다.");
        System.out.println("8진수 12는 10진수로" + 0x12 + "입니다.");
    }
}
